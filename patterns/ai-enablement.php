<!-- wp:html -->
<div class="hp-aie__ringmark"><span class="hp-aie__dot is-water" aria-hidden="true"></span><span class="hp-label-caps">Ring III &middot; Artifact</span></div>
<!-- /wp:html -->

<!-- wp:heading {"level":2} -->
<h2 class="wp-block-heading">Attest &mdash; verifiable provenance</h2>
<!-- /wp:heading -->

<!-- wp:group {"className":"hp-aie__lede","layout":{"type":"default"}} -->
<div class="wp-block-group hp-aie__lede"><!-- wp:paragraph -->
<p>Governance stops at the site boundary. Once an output leaves WordPress, the evidence supporting its origin and integrity has to travel with it or remain recoverable.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The distinction from governance is structural. A governance log gives the owner an internal record. A signed manifest lets an outsider validate a claim&rsquo;s binding to an artifact, identify its signer, and detect alteration &mdash; whether that signer deserves trust is a separate question. It does not make the content true. It makes the claim and later changes testable.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Open C2PA proposals cover <a href="https://github.com/WordPress/ai/pull/459">detecting existing Content Credentials on upload</a> and signing new provenance claims to <a href="https://github.com/WordPress/ai/pull/294">post text</a> and <a href="https://github.com/WordPress/ai/pull/302">images</a>. <a href="https://spec.c2pa.org/specifications/specifications/2.4/specs/C2PA_Specification.html">Durable credentials</a> use additional bindings such as fingerprints or watermarks to help recover a remotely stored manifest when embedded metadata is stripped. This direction aligns with <a href="https://eur-lex.europa.eu/legal-content/EN/TXT/?uri=CELEX:32024R1689">Article 50 of the EU AI Act</a>, scheduled to apply from 2 August 2026, which requires providers of certain generative systems to mark outputs in machine-readable form. C2PA is one possible implementation path, not the legal requirement itself.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:paragraph {"className":"hp-label-caps","style":{"spacing":{"margin":{"top":"var:preset|spacing|8"}}}} -->
<p class="hp-label-caps" style="margin-top:var(--wp--preset--spacing--8)">The principle underneath</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":2} -->
<h2 class="wp-block-heading">The external-instrument test</h2>
<!-- /wp:heading -->

<!-- wp:group {"className":"hp-aie__lede","layout":{"type":"default"}} -->
<div class="wp-block-group hp-aie__lede"><!-- wp:paragraph -->
<p>The same logic extends beyond software: a claim becomes more credible when it survives an instrument the claimant does not control. <a href="https://philosophers.group/disrupting-the-disruptors/">Blake Bertuccelli-Booth&rsquo;s</a> comparison of workforce programs with incubators made the contrast concrete &mdash; administrative wage records and long-running randomized trials on one side, self-reported metrics distorted by survivorship bias on the other.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:quote {"className":"hp-quote is-style-imladris"} -->
<blockquote class="wp-block-quote hp-quote is-style-imladris"><!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size">Real impact is whatever survives an external instrument.</p>
<!-- /wp:paragraph --><cite>The external-instrument test</cite></blockquote>
<!-- /wp:quote -->

<!-- wp:group {"className":"hp-aie__lede","layout":{"type":"default"}} -->
<div class="wp-block-group hp-aie__lede"><!-- wp:paragraph -->
<p>Read through that lens, the three rings form an accountability architecture. Each gives someone other than the author an instrument to read: a schema others can inspect, a log others can audit, a signature a stranger can verify. My ethics position follows the same principle: trust is not a disclaimer appended after the model answers; it is built into the workflow.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>I hold my own work to that standard. I do not ask readers to trust my account of a contribution; I point to current review status, merged changes, and changelog lines, checking GitHub rather than my notes. My portfolio&rsquo;s proof bar renders state, never intent.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:heading {"level":2,"style":{"spacing":{"margin":{"top":"var:preset|spacing|8"}}}} -->
<h2 class="wp-block-heading" style="margin-top:var(--wp--preset--spacing--8)">Putting the friction back</h2>
<!-- /wp:heading -->

<!-- wp:group {"className":"hp-aie__lede","layout":{"type":"default"}} -->
<div class="wp-block-group hp-aie__lede"><!-- wp:paragraph -->
<p>Rapid change does not push me to move faster or guess better. The tooling already does the relentless part &mdash; it will carry a half-formed idea to something that looks shipped without flinching. The scarce skill is knowing where to put the friction back: where a capability needs permission, an action needs a record, an artifact needs a verifiable claim, and my own conclusion needs checking. Each instrument here is exactly that &mdash; friction deliberately placed where a claim needs evidence instead of trust, mine included.</p>
<!-- /wp:paragraph --></div>
