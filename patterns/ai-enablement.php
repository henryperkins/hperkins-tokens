<!-- wp:heading {"level":2} -->
<h2 class="wp-block-heading">Expose &mdash; discoverable capabilities</h2>
<!-- /wp:heading -->

<!-- wp:group {"className":"hp-aie__lede","layout":{"type":"default"}} -->
<div class="wp-block-group hp-aie__lede"><!-- wp:paragraph -->
<p>The foundation has shipped. <a href="https://make.wordpress.org/core/2025/11/10/abilities-api-in-wordpress-6-9/">WordPress 6.9 introduced the Abilities API</a> as a PHP registry through which core, plugins, and themes declare functionality in a standardized, machine-readable format. <a href="https://make.wordpress.org/core/2026/03/24/client-side-abilities-api-in-wordpress-7-0/">WordPress 7.0 added the JavaScript counterpart</a> and integrated abilities with the WP AI Client.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>What is still converging is the agent-facing layer: a foundational <a href="https://github.com/WordPress/ai/issues/40"><code>core/*</code> ability set</a>, <a href="https://github.com/WordPress/ai/issues/430">Skills in the WordPress admin</a>, and the problem of <a href="https://github.com/WordPress/ai/issues/21">exposing thousands of abilities without overwhelming an agent&rsquo;s tool selection</a>. The shape is consistent: a capability declares its contract &mdash; name, description, schemas, permissions, and implementation &mdash; so what WordPress can do is inspectable rather than asserted.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:html -->
<div class="hp-aie__ringmark"><span class="hp-aie__dot is-fire" aria-hidden="true"></span><span class="hp-label-caps">Ring II &middot; Site</span></div>
<!-- /wp:html -->

<!-- wp:heading {"level":2} -->
<h2 class="wp-block-heading">Govern &mdash; owner-controlled use</h2>
<!-- /wp:heading -->

<!-- wp:group {"className":"hp-aie__lede","layout":{"type":"default"}} -->
<div class="wp-block-group hp-aie__lede"><!-- wp:paragraph -->
<p>Discovery alone is not trust. Once an agent can find a capability, the site owner needs to decide whether it may use it and inspect what happened afterward. <a href="https://make.wordpress.org/ai/2026/05/21/whats-new-in-ai-1-0-0/">AI 1.0.0</a>, released 19 May 2026, shipped Request Logging, intended to expose AI activity across core, plugins, and themes, and Connector Approvals, which lets administrators decide which plugins may use configured connectors. A proposed <a href="https://github.com/WordPress/ai/issues/348">Unified AI Management Layer for Core</a> would consolidate permissions, usage metering, and capability-aware provider routing; the proposal recommends deny-by-default for cost safety while acknowledging the compatibility case for allow-by-default.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

