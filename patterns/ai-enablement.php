<div class="wp-block-group hp-aie__lede"><!-- wp:paragraph -->
<p>I came up through WordPress&rsquo;s earlier democratizations, and they rhyme. In 2012, page builders dropped the floor: you assembled a site instead of hand-coding one, and I was running the community around one of them. Blocks repeated the move around 2018: assemble from parts, not markup. AI is the same instinct one level up &mdash; describe the work and point an agent at it. Each wave widened who could build, but also produced fragile sites, because lowering the floor removes friction that once forced inspection. I read this wave through a trust lens: I have watched the floor drop before, and what survives is the discipline someone deliberately puts back in.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>WordPress&rsquo;s AI features look scattered until you see the trust model underneath: exposure, governance, and attestation. Exposure makes WordPress usable by agents; governance makes that use accountable to the owner; attestation makes provenance claims verifiable beyond the site. Read together, these projects suggest one emerging trust model in three widening rings &mdash; capability, site, and artifact &mdash; my synthesis of a direction visible across the public roadmap, not a name the project has adopted.</p>
<!-- /wp:paragraph --></div>
